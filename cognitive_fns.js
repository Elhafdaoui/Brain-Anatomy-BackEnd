const CalculateRMS = function (area, checked_fns) {

    let squares_sum = 0;
    let ticks_nbr = 0;


    Cognitive_Functions[area].map(f => {
        checked_fns.map(e => {

            // if function is ckecked
            if (f.id === e) {
                if (f.ARN) {
                    squares_sum += f.ARN * f.ARN;
                    ticks_nbr += 1
                }
                if (f.PRN) {
                    squares_sum += f.PRN * f.PRN;
                    ticks_nbr += 1
                }
                if (f.RRN) {
                    squares_sum += f.RRN * f.RRN;
                    ticks_nbr += 1
                }
            }

        })
    })

    Mean = squares_sum / ticks_nbr;
    RMS = Math.sqrt(Mean).toFixed(3);
    return (!isNaN(RMS)) ? RMS : 'X.XXX';
}

